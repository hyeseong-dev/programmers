def solution(n):
    return [int(i) for i in reversed(str(n))]


# 위 코드의 시간, 공간 복잡도

# 시간 복잡도(Time Complexity): 이 함수는 먼저 숫자를 문자열로 변환한 다음, 문자열의 각 문자를 거꾸로 순회하여 숫자로 변환합니다. 
# 이 작업의 시간 복잡도는 O(n)입니다. 여기서 n은 숫자를 문자열로 변환한 결과의 길이입니다. str(n)의 복잡도는 O(log(n))이고, 
# 리스트 컴프리헨션은 O(n)이므로, 전체 시간 복잡도는 O(n)이 됩니다.

# 공간 복잡도(Space Complexity): 이 함수는 문자열 버전의 숫자를 저장하고, 거꾸로 된 숫자의 리스트를 반환합니다. 
# 이 두 작업 모두 입력 크기에 비례하는 메모리를 사용하므로, 공간 복잡도는 O(n)입니다.