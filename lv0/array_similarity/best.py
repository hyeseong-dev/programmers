def solution(s1, s2):
    answer = 0
    for s1_item in s1:
        if s1_item in s2:
            answer += 1
    return answer

'''
위 코드의 시간 복잡도는 O(N^2)입니다. 
이유는 s1 배열을 순회하는 for 루프 안에서 s1_item in s2를 확인하기 위해 s2 배열을 순회하는 내부 for 루프가 있기 때문입니다. 
이렇게 하면 s1 배열의 각 요소마다 s2 배열 전체를 순회하므로, 시간 복잡도는 O(N^2)가 됩니다. 
여기서 N은 s1 배열의 길이와 s2 배열의 길이 중 더 큰 값입니다.

공간 복잡도는 O(1)입니다. 입력으로 주어진 배열 외에도 상수 개수의 변수만 사용되므로, 입력 크기에 상관없이 일정한 공간만 사용됩니다.
'''