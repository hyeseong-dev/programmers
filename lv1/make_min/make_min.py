def solution(A, B):
    A.sort()
    B.sort(reverse=True)
    answer = 0
    for i in range(len(A)):
        answer += A[i] * B[i]
    return answer


# 이 문제는 두 개의 배열에서 숫자를 하나씩 선택하여 곱한 값을 더할 때, 그 합이 최소가 되도록 하는 문제입니다.

# 이 문제의 해결 방법은 한 배열은 오름차순으로 정렬하고, 다른 하나는 내림차순으로 정렬하는 것입니다. 그렇게 하면 한 배열에서는 가장 작은 수를, 다른 배열에서는 가장 큰 수를 선택하게 되어 곱한 값이 최소가 됩니다.

# 이 코드에서 A.sort()는 배열 A를 오름차순으로 정렬하고, B.sort(reverse=True)는 배열 B를 내림차순으로 정렬합니다. 그리고 for 문에서는 각 배열에서 숫자를 하나씩 선택하여 곱한 값을 answer에 누적하여 더합니다.

# 예를 들어, solution([1, 4, 2], [5, 4, 4])를 호출하면, 배열 A는 [1, 2, 4]로 정렬되고, 배열 B는 [5, 4, 4]로 정렬됩니다. 그리고 각 배열에서 숫자를 하나씩 선택하여 곱한 값 (1x5, 2x4, 4x4)을 더하면, 총합은 29가 됩니다. 따라서 이 경우 최소값은 29입니다.